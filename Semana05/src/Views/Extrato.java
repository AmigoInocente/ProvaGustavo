    private void carregarExtrato(){
        ArrayList <Transacao> ListaTransacoes = new ArrayList();//VIR DO DAO DAOTransacao.getTodasTransacoes();
        DefaultTableModel table = (DefaultTableModel) this.tabela.getModel();
        //List<> listaTransacoes = makeDAOTransacao.buscarTodasTransacoes();
        
        for(Transacao item: ListaTransacoes){
            Object[] novaLinha = {item.getId(), item.getData(), item.getTipo(), item.getTransacao()};
            table.addRow(novaLinha);
        }
    }
